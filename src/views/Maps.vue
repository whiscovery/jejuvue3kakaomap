<template>
  <div>
      <button @click="toggle">{{ showMap ? "hide" : "show" }}</button>
      <button @click="changeMapWidth">change map width</button>
      <button @click="changeMapHeight">change map height</button>
      <KakaoMap
        v-show="showMap"
        :appkey="appkey"
        :center="center"
        :width="mapWidth"
        :height="mapHeight"
        :isShowing="showMap"
      ></KakaoMap>
  </div>
</template>

<script>
import KakaoMap from '@/components/KakaoMap';
export default {
    data() {
        return  {
            showMap: false,
            appkey:'93e7ae567c188033ab3c4af5d997866a',
            center:[33.450701, 126.570667],
            mapWidth: 200,
            mapHeight: 300
        }
    },

    components: {
        KakaoMap
    },

    // for test
    methods: {
        toggle() {
            this.showMap = !this.showMap;
        },
        changeMapWidth() {
            this.mapWidth = this.mapWidth > 200 ? 200 : 500;
        },
        changeMapHeight() {
            this.mapHeight = this.mapHeight > 300 ? 300 : 800;
        }
    }
}
</script>